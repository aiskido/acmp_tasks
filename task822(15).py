Input = input()
numbers = Input.split()
x1 = int(numbers[0])
y1 = int(numbers[1])
x2 = int(numbers[2])
y2 = int(numbers[3])
x3 = int(numbers[4])
y3 = int(numbers[5])
a = ((x1-x2)**2+(y1-y2)**2)**(1/2)
b = ((x3-x2)**2+(y3-y2)**2)**(1/2)
c = ((x1-x3)**2+(y1-y3)**2)**(1/2)
p = (a+b+c)/2
S = (p*(p-a)*(p-b)*(p-c))**(1/2)
print(round(S, 2))